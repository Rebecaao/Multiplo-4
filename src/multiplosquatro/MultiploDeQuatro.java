package multiplosquatro;

public class MultiploDeQuatro {

	public static void main(String[] args) {

		int numeroRepeticao = 100;
		int contador = 0;
		
		while (contador <= numeroRepeticao) {
			
			contador = contador + 1;
			if (contador % 4 == 0) {
				System.out.println("Multiplos de 4: " + contador  );
				
			} 
		

	}

}

}
